package Strings;

import java.util.*;

public class lengthOfLastWord {
    public static void main(String args[]) {
        String str = new Scanner(System.in).nextLine();
        System.out.println(findLength(str));
    }

    public static int findLength(String str) {
        int length = 0;
        str = str.trim();

        str = str.split(" ");

        return length;
    }
}
